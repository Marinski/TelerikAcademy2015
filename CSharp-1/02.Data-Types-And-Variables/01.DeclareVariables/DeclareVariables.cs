using System;

class DeclareVariables
{
    static void Main()
    {
        ushort firstVal = 52130;
        sbyte secondVal = -115;
        int thirdVal = 4825932;
        byte fourthVal = 97;
        short fifthVal = -10000;

        Console.WriteLine("Data type of {0} is ushort", firstVal);
        Console.WriteLine("Data type of {0} is sbyte", secondVal);
        Console.WriteLine("Data type of {0} is int", thirdVal);
        Console.WriteLine("Data type of {0} is byte", fourthVal);
        Console.WriteLine("Data type of {0} is short", fifthVal);
    }
}